public class loop {
    // Print numbers from 1 to 10 using a for loop
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
    //print numbers from 1 to 10 using while loop
    public static void whileLoop() {
        int i = 1;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }
    }
    
}
